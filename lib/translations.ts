const sortedPhrases = Object.entries({ ...phraseBnToEn, ...exactBnToEn })
  .sort(([left], [right]) => right.length - left.length);

const banglaDigits = ["০", "১", "২", "৩", "৪", "৫", "৬", "৭", "৮", "৯"];

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

export function translateText(value: string, language: Language) {
  if (language === "bn") return value;

  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const core = value.trim();

  if (!core) return value;

  let translated = exactBnToEn[core] ?? core;

  if (translated === core) {
    for (const [bn, en] of sortedPhrases) {
      translated = translated.replace(new RegExp(escapeRegExp(bn), "g"), en);
    }
  }

  translated = translated
    .replace(/[০-৯]/g, (digit) => String(banglaDigits.indexOf(digit)))
    .replace(/৳/g, "BDT ");

  return `${leading}${translated}${trailing}`;
}
